function visualin(dif){
    var vis;
    if (dif==0) {
        vis="Images/visualin.jpg";
    } else if (dif==2){
        vis="Images/visualin1.jpg";
    } else if (dif==3){
        vis="Images/visualin2.jpg";
    }
    document.getElementById("Visual").src = vis;
}

